#max() muestra el mayor valor en el argumento

a, b, c, d, e, f, g, h = 6, 3, 2, 1, 31, 6, 7, 8
mayor = max(a, b, c, d, e, f, g, h)
print('muestra el mayor')
print()
print(mayor)

