#de Estudio de este curso y en los ejemplos de ejercicios y programas resueltos que se
#entreguen a los estudiantes, aplicaremos las siguientes convenciones en cuanto a
#nombres de variables (y de nuevo, aclaramos que los estudiantes e incluso sus
#profesores podrán usar otras convenciones reconocidas si las prefieren. Sólo se sugiere
#que se mantengan consistentes con ellas):

#      El nombre una variable se escribirá siempre en minúsculas (Ejemplos: sueldo, nombre, lado).
#      Cuando se quiera que el nombre de una variable agrupe dos o más palabras, usaremos el guión
#       bajo como separador (Ejemplos: mayor_edad, horas_extra).

#      Excepcionalmente designaremos en mayúsculas sostenidas a alguna variable que represente
#       algún valor muy conocido y normalmente escrito en mayúsculas (Ejemplos: PI (por el número
#pi), o IVA (por nuestro bendito impuesto al valor agregado)).


#- Nunca utilice la l ('letra ele' minúscula) ni la O ('letra o' mayúscula) ni la I ('letra i'
#mayúscula) como nombre simple de una variable: en algunas fuentes de letras esos
#caracteres son indistinguibles de los números 1 y 0 y obviamente causaría confusión.
